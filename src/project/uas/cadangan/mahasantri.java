    private void btcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcariActionPerformed

        String carimhsValue = txtcarimhs.getText();
        try {
            Connection conn = (Connection) conect.konekdb();
            Statement stmt = conn.createStatement();
            ResultSet result = stmt.executeQuery("Select * from menumahasantri"
                    + " WHERE nama ='" + carimhsValue + "'");
            if (result.next()) {
                String namaResult = result.getString("nama");
                String nimResult = result.getString("nim");
                String passResult = result.getString("pass");
                String jurusanResult = result.getString("jurusan");
                String nokamarResult = result.getString("nokamar");

                txtnama.setText(namaResult);
                txtnim.setText(nimResult);
                pw.setText(passResult);
                txtjurusan.setText(jurusanResult);
                txtkamar.setText(nokamarResult);

                JOptionPane.showMessageDialog(null, "Data Mahasiswa "
                        + "" + namaResult + " ditemukan");

//                this.componentForm("cari");
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
//                kosongForm();
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        txtcarimhs.setText("");
    }//GEN-LAST:event_btcariActionPerformed

