    public void tampilData() {
        model = new DefaultTableModel();
        tabelMhs.setModel(model);
        model.addColumn("NIM");
        model.addColumn("Nama Mahasiswa");
        model.addColumn("No. Kamar");
        model.addColumn("Jurusan");
        getData();
    }

    public void getData() {
        try {
            Statement stat = (Statement) conect.konekdb().createStatement();
            String sql = "Select * from menumahasantri";
            ResultSet res = stat.executeQuery(sql);

            while (res.next()) {
                Object[] obj = new Object[4];
                obj[0] = res.getString("nim");
                obj[1] = res.getString("nama");
                obj[2] = res.getString("nokamar");
                obj[3] = res.getString("jurusan");

                model.addRow(obj);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

